                    <p>
                        <br style="color: #3d3d3d;"/><br/>
                        <span style="color: #3d3d3d;"><span style="color: green;"><strong>ชื่อปริญญา</strong></span></span><span
                            style="color: #3d3d3d;">&nbsp;</span><br style="color: #3d3d3d;"/><br/>
                        <span style="color: #3d3d3d;">ชื่อเต็มภาษาไทย วิศวกรรมศาสตรบัณฑิต (วิศวกรรมคอมพิวเตอร์)&nbsp;</span><br
                            style="color: #3d3d3d;"/><br/>
                        <span style="color: #3d3d3d;">ชื่อย่อภาษาไทย วศ.บ. (วิศวกรรมคอมพิวเตอร์)&nbsp;</span><br
                            style="color: #3d3d3d;"/><br/>
                        <span style="color: #3d3d3d;">ชื่อเต็มภาษาอังกฤษ Bachelor of Engineering (Computer Engineering)&nbsp;</span><br
                            style="color: #3d3d3d;"/><br/>
                        <span style="color: #3d3d3d;">ชื่อย่อภาษาอังกฤษ B.Eng. (Computer Engineering)&nbsp;</span><br
                            style="color: #3d3d3d;"/><br/>
                        <br style="color: #3d3d3d;"/><br/>
                        <span style="color: #3d3d3d;"><span
                                style="color: green;"><strong>วัตถุประสงค์&nbsp;</strong></span></span>
                    </p>
                    <ul>
                        <li style="color: #3d3d3d;">
                            เพื่อผลิตวิศวกรปฏิบัติการ
                            ที่มีความสามารถปฏิบัติงานด้านวิศวกรรมคอมพิวเตอร์ได้อย่างมีประสิทธิภาพ
                        </li>
                        <li style="color: #3d3d3d;">
                            เพื่อผลิตวิศวกรที่มีความสามารถปฏิบัติงานในระบบคอมพิวเตอร์
                            และเทคโนโลยีสารสนเทศโดยมีพื้นฐานในด้านการพัฒนาระบบเครือข่ายและการออกแบบ
                            ทั้งสามารถเสนอข้อมูลเกี่ยวกับการดำเนินการออกแบบ ติดตั้งและทดสอบได้
                        </li>
                        <li style="color: #3d3d3d;">
                            เพื่อฝึกฝนให้มีความคิดริเริ่ม มีกิจนิสัยในการค้นคว้าและปรับปรุงตนเองให้ก้าวหน้าอยู่เสมอ
                            สามารถแก้ปัญหาด้วยหลักการและเหตุผล
                            ปฏิบัติงานด้วยหลักวิชาการที่มีการวางแผนและควบคุมอย่างรอบคอบ
                            ซึ่งก่อให้เกิดผลสัมฤทธิ์ในการทำงาน
                        </li>
                        <li style="color: #3d3d3d;">
                            เพื่อเสริมสร้างคุณธรรม ระเบียบวินัย ความซื่อสัตย์สุจริต ความขยันหมั่นเพียร
                            ความสำนึกในจรรยาบรรณแห่งอาชีพ ความรับผิดชอบต่อหน้าที่และสังคม
                        </li>
                    </ul>
