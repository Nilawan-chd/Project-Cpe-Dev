                    <h1 class="entry-title">งานวิจัย / โครงงาน</h1>
                </header><!-- .entry-header -->

                <div class="entry-content">
                    <p>โครงานระดับปริญญาตรี ของสาขาวิชาวิศวรรมคอมพิวเตอร์ มีการจัดแบ่งออกเป็นหลายด้าน ดังต่อไปนี้</p>
                    <ul>
                        <li>ด้านไมโครโพรเซสเซอร์ และการเชื่อมต่อ</li>
                        <li>ด้านการพัฒนาซอฟต์แวร์ ทั้งโมบายและโปรแกรมประยุกต์</li>
                        <li>ด้านการพัฒนาเกมส์ และสื่อการเรียนรู้สมัยใหม่</li>
                        <li>ด้านระบบเครือข่ายและคลาวด์</li>
                        <li>ด้านบูรณาการ</li>
                    </ul>
                    <p>โครงงาน ประจำปีการศึกษา 2560 มีดังนี้</p>
                    <p><a href="http://www.cpe.eng.rmutp.ac.th/wp-content/uploads/2018/06/Amoto.jpg"><img class="alignnone size-medium wp-image-963" src="http://www.cpe.eng.rmutp.ac.th/wp-content/uploads/2018/06/Amoto-300x150.jpg" alt="" width="300" height="150" srcset="http://www.cpe.eng.rmutp.ac.th/wp-content/uploads/2018/06/Amoto-300x150.jpg 300w, http://www.cpe.eng.rmutp.ac.th/wp-content/uploads/2018/06/Amoto-768x385.jpg 768w, http://www.cpe.eng.rmutp.ac.th/wp-content/uploads/2018/06/Amoto-1024x513.jpg 1024w, http://www.cpe.eng.rmutp.ac.th/wp-content/uploads/2018/06/Amoto-500x251.jpg 500w, http://www.cpe.eng.rmutp.ac.th/wp-content/uploads/2018/06/Amoto.jpg 1814w" sizes="(max-width: 300px) 100vw, 300px" /></a></p>
                    <ol>
                        <li>ระบบแจ้งเตือนการเกิดอุบัติเหตุของรถจักรยานยนต์</li>
                        <li>กระจกอัฉริยะ</li>
                        <li>ระบบดูแลผักอัตโนมัติ</li>
                        <li>โรงเรือนเพาะเห็ดอัตโนมัติ</li>
                        <li>ระบบป้องกันภัยในบ้านแจ้งเตือนผ่านไลน์แอปพลิเคชั่น</li>
                        <li>ระบบแจ้งเตือนข่าวสารนักศึกษาผ่านโทรศัพท์เคลื่อนที่</li>
                        <li>เครื่องเจาะแผ่นวงจรพิมพ์อัตโนมัติ</li>
                        <li>เครื่องวิเคราะห์และตรวจสอบไอซี-ทีทีแอล</li>
                        <li>แอปพลิเคชั่นช่วยฝึกอบรมการปฏิบัติงานในองค์กร</li>
                        <li>แอปพลิเคชั่นค้นหาร้านซ่อมรถจักรยานยนต์</li>
                        <li>ระบบตรวจจับบุคคลแจ้งเตือนผ่านโทรศัพท์เคลื่อนที่</li>
                        <li>การออกแบบและจำลองการทำงานของระบบเครือข่ายภายในโรงพยาบาล</li>
                        <li>เกมส์ต่อสู้ 3 มิติ ด้วยไพ่ โดยใช้เทคนิคการค้นหาแบบปฏิปักษ์</li>
                        <li>ระบบควบคุมเครื่องใช้ไฟฟ้า (IOT)</li>
                    </ol>
                </div><!-- .entry-content -->
